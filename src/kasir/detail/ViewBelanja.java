    private void btn_tambah_keranjangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_tambah_keranjangActionPerformed
        // TODO add your handling code here:
        int stok_cek = Integer.parseInt(txt_stok_belanja.getText());
        int jumlah_cek = Integer.parseInt(txt_jumlah_belanja.getText());
        if (jumlah_cek > stok_cek) {
            label_stok_peringatan.setVisible(true); //jika melebihi stok muncul notif 
        } else {
            label_stok_peringatan.setVisible(false);

            DetailPenjualan DP = new DetailPenjualan();

            DP.setId_penjualan(Integer.parseInt(txt_id_penjualan.getText()));
            DP.setId_produk(Integer.parseInt(label_id_produk.getText()));
            DP.setJumlah(Integer.parseInt(txt_jumlah_belanja.getText()));

            BigDecimal harga = new BigDecimal(txt_harga_belanja.getText());
            BigDecimal jumlah = new BigDecimal(txt_jumlah_belanja.getText());
            BigDecimal subTotal = jumlah.multiply(harga); //perkalian
            DP.setSub_total(subTotal);

            CDP.tambahData(DP);

            //mengambil semua total belanja
            BigDecimal total = new BigDecimal(0);
            for (int i = 0; i < tabel_detail_belanja.getRowCount(); i++) {
                BigDecimal a = new BigDecimal(tabel_detail_belanja.getValueAt(i, 4).toString());
                total = total.add(a);
            }

            txt_total_belanja.setText("" + total);

//--------------------------STOK-----------------------------------------------------------------------
            Produk P = new Produk(); //ngambil dri class produk 
            int stok = Integer.parseInt(txt_stok_belanja.getText());  //menguraikan variabel
            int jumlah_belanja = Integer.parseInt(txt_jumlah_belanja.getText());

            int sisaStok = stok - jumlah_belanja; //rumusnya
            P.setStok(sisaStok);
            P.setId_produk(Integer.parseInt(label_id_produk.getText())); //ini ngambil id primary (tersembunyi) nya supaya kd keubah semua datanya

            CDP.updateStok(P, DP);
//-----------------------------------------------------------------------------------------------------
            //reset
            txt_produk_belanja.setText("");
            txt_harga_belanja.setText("");
            txt_stok_belanja.setText("");
            txt_jumlah_belanja.setText("");
        }
    }//GEN-LAST:event_btn_tambah_keranjangActionPerformed

