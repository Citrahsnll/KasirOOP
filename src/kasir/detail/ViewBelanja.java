    private void btn_HapusProdukActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_HapusProdukActionPerformed

//-------------------------HAPUS DATA DITABEL----------------------------------------------------
        int row = tabel_detail_belanja.getSelectedRow();
        String id_detail = tabel_detail_belanja.getValueAt(row, 0).toString();

        CDP.hapusItem(id_detail);
        
//-------------------------- STOK DI CANCEL -------------------------------------------------------------------
        int stok_akhir = Integer.parseInt(label_stok_akhir.getText());
        int jumlah = Integer.parseInt(label_jumlah.getText());
        
        int stok_kembali = stok_akhir + jumlah;
        Produk P = new Produk();
        P.setStok(stok_kembali);
        P.setId_produk(Integer.parseInt(label_id_produk.getText()));
        
        DetailPenjualan DP = new DetailPenjualan();
        DP.setId_penjualan(Integer.parseInt(txt_id_penjualan.getText()));
        CDP.updateStok(P, DP);
        
        CDP.tampilDetailPenjualanBelanja(txt_id_penjualan.getText());
        //ketika tabel belanja diklik, akan muncul data di tabel lain       

        
//----------------------Mengambil Semua Total Belanja------------------------------------------------
        BigDecimal total = new BigDecimal(0);
        for (int i = 0; i < tabel_detail_belanja.getRowCount(); i++) {
            BigDecimal a = new BigDecimal(tabel_detail_belanja.getValueAt(i, 4).toString());
            total = total.add(a);
        }
        txt_total_belanja.setText("" + total);
        
    }//GEN-LAST:event_btn_HapusProdukActionPerformed

