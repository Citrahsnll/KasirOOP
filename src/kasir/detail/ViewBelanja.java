    private void tabel_penjualanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabel_penjualanMouseClicked
        // TODO add your handling code here:
       int row = tabel_penjualan.getSelectedRow();

        txt_id.setText(tabel_penjualan.getValueAt(row, 0).toString());
        combo_id_pel.setSelectedItem(tabel_penjualan.getValueAt(row, 3).toString());
        String combo = combo_id_pel.getSelectedItem().toString();

        String hasil = C.tampil_tamu(combo);
        txt_nama.setText(hasil);

        btn_belanja.setEnabled(true);

        CDP.tampilDetailPenjualanDua(tabel_penjualan.getValueAt(row, 0).toString()); //ketika tabel detail diklik, akan muncul data di tabel lain
        CDP.tampilDetailPenjualanBelanja(tabel_penjualan.getValueAt(row, 0).toString());//ketika tabel belanja diklik, akan muncul data di tabel lain

        //mengambil semua total belanja
        BigDecimal total = new BigDecimal(0);
        for (int i = 0; i < tabel_detail_belanja.getRowCount(); i++) {
            BigDecimal a = new BigDecimal(tabel_detail_belanja.getValueAt(i, 4).toString());
            total = total.add(a);
        }

        txt_total_belanja.setText("" + total);
        txt_total_produk.setText("" + total);
    }//GEN-LAST:event_tabel_penjualanMouseClicked

